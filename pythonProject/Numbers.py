from math import *

num = -5

print(-2.0987)
print(3 + 4.5)
print(3 * (4 + 5))

print(str(num) + " my favorite number")

print(sqrt(36))
# Absolute value.
print(abs(num))
# 3 to the power of 2.
print(pow(3, 2))
# Returns the larger number.
print(max(4, 6))
# Returns the smaller number.
print(min(4, 6))
# Rounds number to nearest whole number.
print(round(3.7))
# Removes any decimal on a whole number.
print(floor(3.7))
# Rounds number up no matter what.
print(ceil(3.2))
