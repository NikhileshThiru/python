phrase = "Giraffe Academy"
print(phrase)

# This is concatenation.
print(phrase + " is cool.")

# This makes the whole string lower case.
print(phrase.lower())
# This makes the whole string upper case.
print(phrase.upper())
# This checks to see if string is all uppercase.
print(phrase.isupper())
# Checks how many characters are inside of the string.
print(len(phrase))
# Checks what character is where.(first character starts with 0)
print(phrase[0])
# Returns the index of the character.
print(phrase.index("G"))
# Replaces Characters or phrases in the string with anything
print(phrase.replace("Giraffe", "Elephant"))

print("Giraffe\nAcademy")

# Use this if you want to print out quotations, python will want to start or end a string.
print("Giraffe\"Academy")
